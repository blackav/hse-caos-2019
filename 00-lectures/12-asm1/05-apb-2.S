// вместо использования глобальных переменных A, B
// значения хранятся на регистрах        
        .att_syntax     noprefix
        .text
        .global main
main:
        call    readi32
        mov     eax, ebx        // ebx = eax, то есть копируем результат в ebx

        // в результате вызова подпрограммы могут потеряться значения
        // хранящиеся в регистрах eax, edx, ecx, поэтому их нельзя
        // использовать в качестве переменных
        // но сохраняются значения остальных регистров: ebx, esi, edi, ebp, esp
        // поэтому для хранения значений можно использовать ebx, esi, edi
        call    readi32

        add     ebx, eax

        call    writei32
        call    nl

        xor     eax, eax
        ret
